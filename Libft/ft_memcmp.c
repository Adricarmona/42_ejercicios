int	ft_memcmp(char *str1, char *str2, int n)
{
	int	i;

i = 0;
	while (0 != n)
	{
	i = *str1 - *str2;
		if (i != 0)
		{
			if (i > 0)
			{
				return (1);
			}
			else if (i < 0)
			{
				return (-1);
			}
		}
	str1++;
	str2++;
	n--;
	}
	return (0);
}
